package Negocio;

public class Diaria {
	String docPagamento;
	int codUniGes;
	int codSubFuncao;
	int codFuncao;
	int codAcao;
	int codProg;
	int codFav;
	int gestaoPag;
	float valorPagamento;
	String dataPagamento;
	
	public String getDocPagamento() {
		return docPagamento;
	}
	public void setDocPagamento(String docPagamento) {
		this.docPagamento = docPagamento;
	}
	public int getCodUniGes() {
		return codUniGes;
	}
	public void setCodUniGes(int codUniGes) {
		this.codUniGes = codUniGes;
	}
	public int getCodSubFuncao() {
		return codSubFuncao;
	}
	public void setCodSubFuncao(int codSubFuncao) {
		this.codSubFuncao = codSubFuncao;
	}
	public int getCodFuncao() {
		return codFuncao;
	}
	public void setCodFuncao(int codFuncao) {
		this.codFuncao = codFuncao;
	}
	public int getCodAcao() {
		return codAcao;
	}
	public void setCodAcao(int codAcao) {
		this.codAcao = codAcao;
	}
	public int getCodProg() {
		return codProg;
	}
	public void setCodProg(int codProg) {
		this.codProg = codProg;
	}
	public int getCodFav() {
		return codFav;
	}
	public void setCodFav(int codFav) {
		this.codFav = codFav;
	}
	public int getGestaoPag() {
		return gestaoPag;
	}
	public void setGestaoPag(int gestaoPag) {
		this.gestaoPag = gestaoPag;
	}
	public float getValorPagamento() {
		return valorPagamento;
	}
	public void setValorPagamento(float valorPagamento) {
		this.valorPagamento = valorPagamento;
	}
	public String getDataPagamento() {
		return dataPagamento;
	}
	public void setDataPagamento(String dataPagamento) {
		this.dataPagamento = dataPagamento;
	}
	
	
}
