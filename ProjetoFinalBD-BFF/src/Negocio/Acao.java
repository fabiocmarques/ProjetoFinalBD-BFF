package Negocio;

public class Acao {
	String codAcao;
	String nome;
	String linguagemCidada;
	
	public String getCodAcao() {
		return codAcao;
	}
	public void setCodAcao(String codAcao) {
		this.codAcao = codAcao;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getLinguagemCidada() {
		return linguagemCidada;
	}
	public void setLinguagemCidada(String linguagemCidada) {
		this.linguagemCidada = linguagemCidada;
	}
}
